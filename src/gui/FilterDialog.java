    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String znacka = jComboBoxZnacka.getSelectedItem().toString();
        int kmOd = Integer.parseInt(jTextFieldNajazdeneKmOD.getText());
        int kmDo = Integer.parseInt(jTextFieldNajazdeneKmDO.getText());
        int rvOd = Integer.parseInt(jTextFieldRokVyrobyOD.getText());
        int rvDo = Integer.parseInt(jTextFieldRokVyrobyDO.getText());
        int cenaOd = Integer.parseInt(jTextFieldCenaOd.getText());
        int cenaDo = Integer.parseInt(jTextFieldCenaDo.getText());
        for (Inzerat inzerat : inzeraty) {
            if(!inzerat.getKategoria().getZnacka().equalsIgnoreCase(znacka)
                    && jComboBoxZnacka.getSelectedIndex() != 0){
                inzeraty.remove(inzerat);
            }else if(inzerat.getKategoria().getNajazdeneKM() < kmOd 
                    && inzerat.getKategoria().getNajazdeneKM() > kmDo){
                inzeraty.remove(inzerat);
            }else if(inzerat.getKategoria().getRokVyroby() < rvOd 
                    && inzerat.getKategoria().getRokVyroby() > rvDo){
                inzeraty.remove(inzerat);
            }else if(inzerat.getKategoria().getCena()< cenaOd 
                    && inzerat.getKategoria().getCena() > cenaDo){
                inzeraty.remove(inzerat);
            }
        }
        dispose();
        
    }//GEN-LAST:event_jButton1ActionPerformed


