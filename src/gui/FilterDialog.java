    void NaplnUdaje() {
        ArrayList<String> znacky = new ArrayList<>();
        for (Inzerat inzerat : inzeraty) {
            if(!znacky.contains(inzerat.getKategoria().getZnacka())) {
                znacky.add(inzerat.getKategoria().getZnacka());
            } 
        }
        for (String string : znacky) {
            jComboBoxZnacka.addItem(string);
        }
        jTextFieldNajazdeneKmDO.setText("");
        jTextFieldNajazdeneKmOD.setText("");
        jTextFieldRokVyrobyDO.setText("");
        jTextFieldRokVyrobyOD.setText("");
        jTextFieldCenaDo.setText("");
        jTextFieldCenaOd.setText("");
                
    }
