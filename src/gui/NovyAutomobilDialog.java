    public boolean validaciaFormularu(){
        if(jTextFieldCena.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nezadaná cena automobilu");
            return false;
        }else if(jTextFieldZnacka.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nezadaná značka automobilu");
            return false;
        }else if(jTextFieldModel.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nezadaný model automobilu");
            return false;
        }else if(jTextFieldRokVyroby.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nezadaný rok výroby automobilu");
            return false;
        }else if(jTextFieldVykon.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nezadaný výkon automobilu");
            return false;
        }else if(jTextFieldPocetKM.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nezadaný počet kilometrov automobilu");
            return false;
        }else if(jTextFieldKaroseria.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nezadaný typ karosérie automobilu");
            return false;
        }else if(jComboBoxPocetSedadiel.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Nezadaný počet sedadiel automobilu");
            return false;
        }else if(jComboBoxStavVozidla.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Nezadaný stav automobilu");
            return false;
        }else if(jComboBoxPocetDveri.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Nezadaný počet dverí automobilu");
            return false;
        }else if(jComboBoxPalivo.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Nezadaný typ paliva automobilu");
            return false;
        }
        return true;
    }
    
