    private void jButtonUlozitInzeratActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUlozitInzeratActionPerformed
        //inicializacia premennych  
    
        if(validaciaFormularu()){
            int cena = Integer.parseInt(jTextFieldCena.getText());
            String znacka = jTextFieldZnacka.getText();
            String model = jTextFieldModel.getText();       
            String palivo = jComboBoxPalivo.getSelectedItem().toString();                               
            int rokVyroby = Integer.parseInt(jTextFieldRokVyroby.getText());
            StavVozidla stavVozidla = StavVozidla.valueOf(jComboBoxStavVozidla.getSelectedItem().toString());
            int vykon = Integer.parseInt(jTextFieldVykon.getText());
            int pocetKm = Integer.parseInt(jTextFieldPocetKM.getText());
            int pocetDveri = Integer.parseInt(jComboBoxPocetDveri.getSelectedItem().toString());
            int pocetSedadiel = Integer.parseInt(jComboBoxPocetSedadiel.getSelectedItem().toString());
            String karoseria = jTextFieldKaroseria.getText();
            String popis = "";
            popis = jTextAreaPopis.getText();
            if(upravaVozidla){
                automobil.setZnacka(znacka);
                automobil.setModel(model);
                automobil.setCena(cena);
                automobil.setKaroseria(karoseria);
                automobil.setNajazdeneKM(pocetKm);
                automobil.setPalivo(palivo);
                automobil.setPocetDveri(pocetDveri);
                automobil.setPopis(popis);
                automobil.setPocetSedadiel(pocetSedadiel);
                automobil.setStav(stavVozidla);
                automobil.setVykon(vykon);
                automobil.setRokVyroby(rokVyroby);
                JOptionPane.showMessageDialog(null, "Informácie o automobile boli obnovené", "Upozornenie", JOptionPane.INFORMATION_MESSAGE);   
                dispose();
            }else{
                //vytvorenie instancie automobil
                automobil = new Automobil(karoseria, pocetDveri, pocetSedadiel, cena, model, pocetKm, palivo, rokVyroby, stavVozidla, vykon, znacka, popis);
                dispose();
            }
        }      
    }//GEN-LAST:event_jButtonUlozitInzeratActionPerformed


